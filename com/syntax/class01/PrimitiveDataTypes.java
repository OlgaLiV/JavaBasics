package com.syntax.class01;

public class PrimitiveDataTypes {
	
	public static void main (String [] args) {
		
		/* Primitive Data Tipes
		 * byte
		 * short
		 * int
		 * long
		 * float
		 * double
		 * char
		 * boolean		
		 */
		// Whole numbers of Data Types
		byte num = 1;
		short num1 = 100;
		int num2 = 1000;
		//l or L
		long num3 = 1000007804903456l;
		
		//Decimal Number
		//f or F
		float num4 = 12.9F;
		double num5 = 0.888;
		
		//Non-Number Data Types
		
		char add = '+';
		char letter = 'a';
		
		//true or false (yes or no)
		//Do not put Yes or No for value
		boolean hungry = true;
		boolean thirsty = false;
	
		
		
	}

}
