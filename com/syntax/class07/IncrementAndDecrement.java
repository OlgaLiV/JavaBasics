package com.syntax.class07;

public class IncrementAndDecrement {
	public static void main(String[] args) {
        int z = 5;
        if (++z == 6) {
        	System.out.println("Hi there!");
        }
        System.out.println(z);
        
        System.out.println("---------------");
        int k = 10;
        if(k++ == 11) {
        	System.out.println();
        }
        
	}
}
